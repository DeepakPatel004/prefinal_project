type Strings = typeof defaultStrings.en;

/**
 * Helper to normalize language codes coming from storage or user input.
 */
function normalizeLang(raw?: string | null): Lang {
  if (!raw) return 'en';
  const s = String(raw).trim().toLowerCase();
  if (s.startsWith('en')) return 'en';
  if (s.startsWith('hi')) return 'hi';
  if (s.startsWith('gu')) return 'gu';
  return 'en';
}

type TFunc = (key: string, vars?: Record<string, string | number>) => string;

const I18nContext = createContext({
  lang: 'en' as Lang,
  setLang: (l: Lang) => {},
  t: ((k: string) => k) as TFunc,
});

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    const s = localStorage.getItem(STORAGE_KEY);
    return normalizeLang(s);
  });

  useEffect(() => {
    try {
      localStorage.setItem(STORAGE_KEY, lang);
    } catch (e) {
      // ignore storage errors in some environments
    }
    // mark document language for accessibility
    try {
      document.documentElement.lang = lang;
    } catch (e) {}
  }, [lang]);

  const setLang = useCallback((l: Lang) => setLangState(normalizeLang(l)), []);

  const t: TFunc = useCallback((key: string, vars?: Record<string, string | number>) => {
    // Try current language
    const langObj = (defaultStrings as any)[lang] || {};
    if (langObj && Object.prototype.hasOwnProperty.call(langObj, key) && langObj[key]) {
      let out = String(langObj[key]);
      if (vars) {
        for (const [k, v] of Object.entries(vars)) {
          out = out.split(`{${k}}`).join(String(v));
        }
      }
      return out;
    }

    // Fallback to English
    const enVal = (defaultStrings as any)['en']?.[key];
    if (enVal) {
      let out = String(enVal);
      if (vars) {
        for (const [k, v] of Object.entries(vars)) {
          out = out.split(`{${k}}`).join(String(v));
        }
      }
      return out;
    }

    // Final fallback: search any language
    for (const k of Object.keys(defaultStrings)) {
      const obj = (defaultStrings as any)[k];
      if (obj && Object.prototype.hasOwnProperty.call(obj, key) && obj[key]) {
        let out = String(obj[key]);
        if (vars) {
          for (const [vk, vv] of Object.entries(vars)) {
            out = out.split(`{${vk}}`).join(String(vv));
          }
        }
        return out;
      }
    }

    // Nothing found — return key as last resort
    return key;
  }, [lang]);

  return (
    <I18nContext.Provider value={{ lang, setLang, t }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}

export default I18nContext;
